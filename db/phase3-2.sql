-- 3) แก้ policy profiles_select — หัวหน้าเห็นสมาชิกทีมที่ตัวเองดูแล
--
-- ช่องโหว่ที่ step 2.4 ทิ้งไว้:
--   policy เดิมเขียน team_id = my_team_id() ตรง ๆ ไม่ผ่าน can_access_team()
--   → หัวหน้าที่ได้สิทธิ์ดู GOV.1/3/4 เห็น "งาน" ของสามทีมครบ แต่เห็น "คน" แค่ทีมตัวเอง
--     หน้าทีมขาย (step 3.2) จึงขึ้นว่าทีมอื่นมีสมาชิก 0 คน ทั้งที่จริงมี
--
-- ⭐ กลับมาเข้ากติกาเดิมของโปรเจกต์: ทุกตารางถามสิทธิ์ผ่าน can_access_team() ตัวเดียว
--    เพิ่ม/ลดสิทธิ์หัวหน้าทีหลัง แก้ที่ฟังก์ชันเดียว ทั้งระบบตามหมด
--
-- ขอบเขตที่เปลี่ยน: หัวหน้าอ่านได้กว้างขึ้นเท่านั้น
--   sale เหมือนเดิมทุกประการ (can_access_team คืน true ให้ทีมตัวเองอยู่แล้ว)
--   สิทธิ์ "แก้" ไม่ได้แตะ — profiles_update ยังเป็น ตัวเอง หรือ admin เท่านั้น
--
-- id = auth.uid() ต้องอยู่เงื่อนไขแรกเสมอ ไม่งั้นคนที่ยังไม่ถูกจัดทีม (team_id ว่าง)
-- จะมองไม่เห็นแม้แต่โปรไฟล์ตัวเอง แล้วล็อกอินเข้ามาเจอหน้าเปล่า
-- ══════════════════════════════════════════════════════════

drop policy if exists profiles_select on profiles;

create policy profiles_select on profiles
  for select to authenticated
  using (
    id = auth.uid()
    or is_admin()
    or can_access_team(team_id)
  );

-- ══════════════════════════════════════════════════════════
-- 4) ตรวจผลหลังรัน
-- ══════════════════════════════════════════════════════════

select 'คอลัมน์ playbook' as check_item,
       count(*)::text as result, '1 expected' as note
from information_schema.columns
where table_schema = 'public' and table_name = 'lead_sources' and column_name = 'playbook'

union all
select 'เส้นทางที่มี playbook แล้ว', count(*)::text, '8 expected'
from lead_sources where playbook is not null and length(playbook) > 0

union all
select 'profiles_select ใช้ can_access_team', count(*)::text, '1 expected'
from pg_policies
where schemaname = 'public' and tablename = 'profiles'
  and policyname = 'profiles_select' and qual like '%can_access_team%'

union all
select 'ทีมขายทั้งหมด', count(*)::text, '5 expected'
from teams where is_active;
